import mongoose from 'mongoose';

export const connectDB = async (): Promise<void> => {
  try {
    const mongoUri = process.env.MONGODB_URI;
    
    if (!mongoUri) {
      throw new Error('MONGODB_URI environment variable is not defined');
    }

    // Replace <db_password> placeholder if it exists
    const finalUri = mongoUri.includes('<db_password>') 
      ? mongoUri.replace('<db_password>', process.env.DB_PASSWORD || '')
      : mongoUri;

    await mongoose.connect(finalUri, {
      maxPoolSize: 10,
      serverSelectionTimeoutMS: 5000,
      socketTimeoutMS: 45000,
      family: 4, // Use IPv4, skip trying IPv6
    });

    console.log('âœ… MongoDB connected successfully to Insurance_samadan_db');
    console.log('ðŸ“Š Database:', mongoose.connection.db.databaseName);
  } catch (error) {
    console.error('âŒ MongoDB connection error:', error);
    process.exit(1);
  }
};

// Handle connection events
mongoose.connection.on('error', (error) => {
  console.error('âŒ MongoDB connection error:', error);
});

mongoose.connection.on('disconnected', () => {
  console.log('âš ï¸  MongoDB disconnected');
});

mongoose.connection.on('reconnected', () => {
  console.log('âœ… MongoDB reconnected');
});

process.on('SIGINT', async () => {
  await mongoose.connection.close();
  console.log('ðŸ”’ MongoDB connection closed through app termination');
  process.exit(0);
});